#include "main.h"

/**
 * add - function that adds two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of the integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: difference of the two integers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: the product of the two integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides an integer by anotjjer integer
 * @a: integer
 * @b: divisor
 * Return: the quotient of the two integers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds the modulo of two integers
 * @a: first integer
 * @b: second integer
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
